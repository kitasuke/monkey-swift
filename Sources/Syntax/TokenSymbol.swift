public enum TokenSymbol: Character {
    case equal = "="
    case plus = "+"
    case minus = "-"
    case bang = "!"
    case asterisk = "*"
    case slash = "/"
    case comma = ","
    case colon = ":"
    case semicolon = ";"
    case leftParen = "("
    case rightParen = ")"
    case leftBrace = "{"
    case rightBrace = "}"
    case leftBracket = "["
    case rightBracket = "]"
    case lessThan = "<"
    case greaterThan = ">"
    case doubleQuatation = "\""
}
